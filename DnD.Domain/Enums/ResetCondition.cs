namespace DnD.Domain.Enums;

public enum ResetCondition
{
    None = 0, // Не відновлюється автоматично (Стріли, Зілля)
    ShortRest = 1, // Відновлюється під час Короткого Відпочинку (Action Surge)
    LongRest = 2, // Відновлюється під час Довгого Відпочинку (Лють, Слоти магії)
    Dawn = 3, // Відновлюється на світанку (Магічні предмети - опціонально)
}
